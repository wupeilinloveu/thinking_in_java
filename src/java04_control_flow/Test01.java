package java04_control_flow;

/**
 * 写一个程序，打印从1到100的值。
 * Created by Emily on 2021/9/8
 */
public class Test01 {
    public static void test(){
        for(int i=0;i<=100;i++){
            System.out.println("从1到100的值为："+i);
        }
    }

    public static void main(String[] args) {
        test();
    }
}
